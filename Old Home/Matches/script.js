let currentSeason = season2;

function showSeason(season) {
  currentSeason = season === 2 ? season2 : [];
  filterMatches();
}

function filterMatches() {
  const search = document.getElementById("searchInput").value.toLowerCase();
  const resultFilter = document.getElementById("resultFilter").value;

  const filtered = [...currentSeason]
    .reverse()
    .filter(match =>
      match.opponent.toLowerCase().includes(search) &&
      (resultFilter === "all" || match.result === resultFilter)
    );

  displayMatches(filtered);
  showStats(currentSeason);
}

function displayMatches(matches) {
  const list = document.getElementById("matchList");
  list.innerHTML = "";

  matches.forEach((match) => {
    const card = document.createElement("div");
    card.className = `match-card ${match.result}`;
    card.innerHTML = `
      <img src="${match.image}" alt="${match.opponent}" onclick="openLightbox('${match.image}')"/>
      <div class="match-info">
        <p><strong>Match #${match.number}</strong></p>
        <p>Date: ${match.date}</p>
        <p>Opponent: ${match.opponent}</p>
        <p>Result: ${match.result.toUpperCase()}</p>
        <p>Score: ${match.score}</p>
        <p>Goals: ${match.goals}</p>
        <p>Assists: ${match.assists}</p>
        <p>MOTM: ${match.motm}</p>
        <button class="details-button" onclick="toggleDescription(this)">View Description</button>
        <div class="description-box">${match.description}</div>
      </div>
    `;
    list.appendChild(card);
  });
}

function showStats(matches) {
  const stats = {
    won: matches.filter(m => m.result === "won").length,
    lost: matches.filter(m => m.result === "lost").length,
    draw: matches.filter(m => m.result === "draw").length
  };

  const statsDiv = document.getElementById("stats");
  statsDiv.innerHTML = `
    <p style="color:#00ff00;">Total Won: ${stats.won}</p>
    <p style="color:#ff4444;">Total Lost: ${stats.lost}</p>
    <p style="color:#ffff00;">Total Draw: ${stats.draw}</p>
  `;
}

function toggleDescription(btn) {
  const box = btn.nextElementSibling;
  box.style.display = box.style.display === "block" ? "none" : "block";
}

function openLightbox(src) {
  document.getElementById("lightbox").style.display = "flex";
  document.getElementById("lightbox-img").src = src;
}

function closeLightbox() {
  document.getElementById("lightbox").style.display = "none";
  document.getElementById("lightbox-img").src = "";
}

window.onload = () => {
  filterMatches();
};
