function displayMembers(filteredMembers = members) {
    const membersSection = document.getElementById('clubMembers');
    membersSection.innerHTML = '';

    filteredMembers.forEach(member => {
        membersSection.appendChild(createMemberCard(member));
    });
}

function createMemberCard(member) {
    const memberCard = document.createElement('div');
    memberCard.classList.add('member-card');

    memberCard.innerHTML = `
        <div class="member-info">
            <img src="${member.profilePicture}" alt="${member.name}">
            <div class="member-details">
                <h3>${member.name}</h3>
                <p class="member-number" style="display: none;">Member Number: ${member.number}</p>
            </div>
        </div>
    `;

    return memberCard;
}

function searchMembers() {
    const searchTerm = document.getElementById('searchMember').value.toLowerCase();
    const filteredMembers = members.filter(member => member.name.toLowerCase().includes(searchTerm));
    displayMembers(filteredMembers);
}

