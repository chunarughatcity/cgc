// Function to display players
function displayPlayers() {
    const playersSection = document.getElementById('players');
    playersSection.innerHTML = '';

    players.forEach(player => {
        playersSection.appendChild(createPlayerCard(player));
    });
}

// Function to create player card
function createPlayerCard(player) {
    const playerCard = document.createElement('div');
    playerCard.classList.add('player-card');

    playerCard.innerHTML = `
        <div class="player-info">
            <img src="${player.profilePicture}" alt="${player.name}">
            <div class="player-details">
                <h3 id="name-${player.id}">${player.name}</h3>
                <p>Position: <span class="position">${player.position}</span></p>
                <p>Goals: <span class="goals">${player.goals}</span></p>
                <p>Assists: <span class="assists">${player.assists}</span></p>
                <p>Yellow Cards: <span class="yellow-cards">${player.yellowCards}</span></p>
                <p>Red Cards: <span class="red-cards">${player.redCards}</span></p>
                <p>MOTM Count: <span class="motm-count">${player.motmCount}</span></p>
                <p>Appearances: <span class="appearances">${player.appearances}</span></p>
            </div>
        </div>
        <div class="player-number-container">
            <div class="player-number">${player.playerNumber}</div>
        </div>
    `;

    playerCard.addEventListener('click', () => showFullView(player));

    return playerCard;
}

// Function to show full view of player card
function showFullView(player) {
    const overlay = document.getElementById('overlay');
    overlay.classList.add('active');

    const fullViewCard = document.getElementById('fullViewCard');
    fullViewCard.innerHTML = `
        <button class="close-btn" onclick="closeFullView()">Ã—</button>
        <img src="${player.profilePicture}" alt="${player.name}">
        <h3>${player.name}</h3>
        <p>Position: <span class="position">${player.position}</span></p>
        <p>Goals: <span class="goals">${player.goals}</span></p>
        <p>Assists: <span class="assists">${player.assists}</span></p>
        <p>Yellow Cards: <span class="yellow-cards">${player.yellowCards}</span></p>
        <p>Red Cards: <span class="red-cards">${player.redCards}</span></p>
        <p>MOTM Count: <span class="motm-count">${player.motmCount}</span></p>
        <p>Appearances: <span class="appearances">${player.appearances}</span></p>
        <div class="player-number-container">
            <div class="player-number">${player.playerNumber}</div>
        </div>
    `;
}

// Function to close full view
function closeFullView() {
    const overlay = document.getElementById('overlay');
    overlay.classList.remove('active');
}

// Function to search players by name
function searchPlayers() {
    const searchInput = document.getElementById('searchPlayerInput').value.toLowerCase();
    const filteredPlayers = players.filter(player => player.name.toLowerCase().includes(searchInput));
    
    const playersSection = document.getElementById('players');
    playersSection.innerHTML = '';

    filteredPlayers.forEach(player => {
        playersSection.appendChild(createPlayerCard(player));
    });
}

